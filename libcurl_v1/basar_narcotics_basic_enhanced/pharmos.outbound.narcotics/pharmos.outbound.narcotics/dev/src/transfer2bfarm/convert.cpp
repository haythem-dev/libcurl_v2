basar::Int32 Convert::toBasarInt32(const basar::VarString& number)
{
	return number.stoi();
}

//--------------------------------------------------------------------------------------------------//
basar::Int32 Convert::toBasarInt32(const QString& number)
{
	return number.toInt();
}

//--------------------------------------------------------------------------------------------------//
basar::VarString Convert::toVarString(const basar::Int16 number)
{
	basar::Int32 number32(number);

	return toVarString(number32);
}

//--------------------------------------------------------------------------------------------------//
basar::VarString Convert::toVarString(const basar::Int32 number)
{
	std::stringstream ss;

	ss << number;
	
	basar::VarString str( ss.str().c_str());	

	return str;
}

